public class PairPoint {
    public Point p1;
    public Point p2;
    
    public PairPoint() {
        p1 = null; 
        p2 = null;
    }
    
    public PairPoint(Point _p1, Point _p2) {
        p1 = _p1;
        p2 = _p2;
    }
    
    
}
